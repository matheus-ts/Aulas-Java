    private String calculo(){
    
        String resultado;
        
        Integer peso = Integer.parseInt(txtPeso.getText());
        Integer altura = Integer.parseInt(txtAltura.getText());
        
        Integer resultadoAltura = altura*altura;
        Integer resultadoTotal = resultadoAltura / peso;  
      
      
      resultado = resultadoTotal.toString();
  
      return resultado;
      
    }
